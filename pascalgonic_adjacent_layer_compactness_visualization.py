def build_bit_angles(p: int, rotation_deg: float = 90.0):
    """
    Example for p=5 and rotation_deg=90:
        00001 -> 90°
        00010 -> 162°
        00100 -> 234°
        01000 -> 306°
        10000 -> 18°
    """
    angles = np.linspace(0, 2 * np.pi, p, endpoint=False) + np.deg2rad(rotation_deg)
    return {1 << i: normalize_angle(angles[i]) for i in range(p)}


def mask_direction_angle(mask: int, bit_angles: dict) -> float:
    vx, vy = 0.0, 0.0
    for bit, ang in bit_angles.items():
        if mask & bit:
            vx += math.cos(ang)
            vy += math.sin(ang)

    if abs(vx) < 1e-12 and abs(vy) < 1e-12:
        return 0.0

    return normalize_angle(math.atan2(vy, vx))


def minor_arc_midpoint(a: float, b: float) -> float:
    a = normalize_angle(a)
    b = normalize_angle(b)
    diff = (b - a) % (2 * math.pi)

    if diff <= math.pi:
        return normalize_angle(a + 0.5 * diff)

    return normalize_angle(b + 0.5 * ((a - b) % (2 * math.pi)))


def visual_angle_for_mask(mask: int, bit_angles: dict) -> float:
    """
    Visual angle for explanatory panel:
    - singleton in R1: bit direction
    - 2-element subset in R2: midpoint between two singleton directions
    - higher layers: vector-sum direction
    """
    bits = bits_of_mask(mask)

    if len(bits) == 1:
        return bit_angles[bits[0]]

    if len(bits) == 2:
        a1 = bit_angles[bits[0]]
        a2 = bit_angles[bits[1]]
        return minor_arc_midpoint(a1, a2)

    return mask_direction_angle(mask, bit_angles)


def initial_ring_order(masks, bit_angles, rotation_deg=90.0):
    return sorted(
        masks,
        key=lambda m: cyclic_offset_from_start(
            mask_direction_angle(m, bit_angles),
            rotation_deg
        )
    )


def build_partition_centers(m: int, rotation_deg: float = 90.0):
    sector_width = 2 * math.pi / m
    start_center = np.deg2rad(rotation_deg)
    return [normalize_angle(start_center + j * sector_width) for j in range(m)]


