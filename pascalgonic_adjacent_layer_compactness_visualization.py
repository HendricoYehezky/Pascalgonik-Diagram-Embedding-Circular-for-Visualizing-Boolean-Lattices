def is_prime(n: int) -> bool:
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0:
        return False
    r = int(math.isqrt(n))
    for i in range(3, r + 1, 2):
        if n % i == 0:
            return False
    return True


def popcount(x: int) -> int:
    return bin(x).count("1")


def mask_to_binary(mask: int, p: int) -> str:
    return format(mask, f"0{p}b")


def binary_to_mask(binary: str) -> int:
    return int(binary, 2)


def masks_in_layer(p: int, k: int):
    return [m for m in range(2 ** p) if popcount(m) == k]


def normalize_angle(theta: float) -> float:
    return theta % (2 * math.pi)


def circular_angle_distance(a: float, b: float) -> float:
    d = abs(normalize_angle(a - b))
    return min(d, 2 * math.pi - d)


def circular_slot_distance(i: int, j: int, m: int) -> int:
    d = abs(i - j)
    return min(d, m - d)


def pol2cart(r: float, theta: float):
    return r * math.cos(theta), r * math.sin(theta)


def angle_deg(theta: float) -> float:
    return math.degrees(normalize_angle(theta))


def bits_of_mask(mask: int):
    bits = []
    b = 1
    x = mask
    while x:
        if x & 1:
            bits.append(b)
        x >>= 1
        b <<= 1
    return bits


def ha_va_from_angle(theta: float):
    x, y = math.cos(theta), math.sin(theta)

    if x > 0.25:
        ha = "left"
    elif x < -0.25:
        ha = "right"
    else:
        ha = "center"

    if y > 0.25:
        va = "bottom"
    elif y < -0.25:
        va = "top"
    else:
        va = "center"

    return ha, va


def cyclic_offset_from_start(theta: float, start_deg: float) -> float:
    start = np.deg2rad(start_deg)
    return normalize_angle(theta - start)


def layer_cap_formula(p: int) -> int:
    """
    User-requested cap:
        ceil((2^p - 2) / (p - 1))
    """
    if p <= 1:
        return 0
    return math.ceil((2 ** p - 2) / (p - 1))


def max_valid_first_layer(p: int) -> int:
    """
    Adjacent layers are (R_k, R_{k+1}), so the first layer k must satisfy:
        k >= 1
        k <= p - 2
    and also respect the requested cap formula.
    """
    cap_formula = layer_cap_formula(p)
    return max(1, min(p - 2, cap_formula - 1))


def choose_default_target_binary(p: int, k: int) -> str:
    """
    Pick a default target of size k:
        p=5, k=1 -> 00001
        p=5, k=2 -> 00011
        p=5, k=3 -> 00111
    """
    k = max(0, min(k, p))
    return ("0" * (p - k)) + ("1" * k)


