def draw_guides_between_layers(ax, outer_masks, bit_angles, inner_radius, outer_radius, color="#a3a3a3"):
    """
    Simple visual guide lines from inner ring toward directions of outer ring elements.
    """
    for mk in outer_masks:
        th = visual_angle_for_mask(mk, bit_angles)
        x0, y0 = pol2cart(inner_radius + 0.04, th)
        x1, y1 = pol2cart((inner_radius + outer_radius) / 2.0, th)
        ax.plot([x0, x1], [y0, y1], color=color, lw=0.80, ls=":", alpha=0.95, zorder=2)


