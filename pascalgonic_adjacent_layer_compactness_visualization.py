def draw_radial_arrow(ax, theta, r0, r1, color="black", lw=1.5, ls="-", alpha=1.0):
    x0, y0 = pol2cart(r0, theta)
    x1, y1 = pol2cart(r1, theta)
    ax.annotate(
        "",
        xy=(x1, y1),
        xytext=(x0, y0),
        arrowprops=dict(
            arrowstyle="->",
            lw=lw,
            color=color,
            linestyle=ls,
            alpha=alpha,
            shrinkA=0,
            shrinkB=0,
            mutation_scale=10,
        ),
    )


def tangential_rotation_deg(theta_rad: float) -> float:
    rot = angle_deg(theta_rad) - 90.0
    if rot < -90:
        rot += 180
    if rot > 90:
        rot -= 180
    return rot


def font_size_for_sector_count(m: int, base=10):
    if m <= 5:
        return base
    if m <= 8:
        return base - 1
    if m <= 12:
        return base - 2
    return max(6, base - 3)


def draw_ring_partition_lines(
    ax,
    centers,
    r_inner,
    r_outer,
    color="#9a9a9a",
    lw=0.9,
    ls="--",
    alpha=0.8,
):
    for c in centers:
        x0, y0 = pol2cart(r_inner, c)
        x1, y1 = pol2cart(r_outer, c)
        ax.plot([x0, x1], [y0, y1], color=color, lw=lw, ls=ls, alpha=alpha, zorder=3)


def add_polyline_label(ax, text, anchor_xy, mid1_xy, mid2_xy, text_xy, ha="left"):
    xs = [anchor_xy[0], mid1_xy[0], mid2_xy[0], text_xy[0]]
    ys = [anchor_xy[1], mid1_xy[1], mid2_xy[1], text_xy[1]]
    ax.plot(xs, ys, color="#555555", lw=1.0, zorder=18)
    ax.text(
        text_xy[0],
        text_xy[1],
        text,
        ha=ha,
        va="center",
        fontsize=12,
        fontweight="semibold",
        color="#333333",
        zorder=19,
        bbox=dict(
            boxstyle="round,pad=0.12",
            fc="white",
            ec="#d9d9d9",
            lw=0.4,
            alpha=0.96,
        ),
    )


def draw_sector_ring(
    ax,
    centers,
    labels,
    related_flags,
    r_inner,
    r_outer,
    label_mode="horizontal",
    title=None,
):
    m = len(centers)
    sector_width_deg = 360 / m
    sector_width_rad = 2 * math.pi / m
    fs = font_size_for_sector_count(m, base=9.5)

    for j, center in enumerate(centers):
        theta_deg = angle_deg(center)
        theta1 = theta_deg - sector_width_deg / 2
        theta2 = theta_deg + sector_width_deg / 2

        facecolor = "#cfaa45" if related_flags[j] else "#ededed"

        wedge = Wedge(
            (0, 0),
            r_outer,
            theta1,
            theta2,
            width=(r_outer - r_inner),
            facecolor=facecolor,
            edgecolor="#222222",
            linewidth=1.0,
            zorder=1,
        )
        ax.add_patch(wedge)

    draw_ring_partition_lines(ax, centers, r_inner, r_outer)

    for j, center in enumerate(centers):
        rl = r_inner + 0.47 * (r_outer - r_inner)
        if sector_width_rad < 0.75:
            rl = r_inner + 0.44 * (r_outer - r_inner)

        xl, yl = pol2cart(rl, center)
        rotation = 0 if label_mode == "horizontal" else tangential_rotation_deg(center)

        ax.text(
            xl,
            yl,
            labels[j],
            ha="center",
            va="center",
            fontsize=fs,
            rotation=rotation,
            rotation_mode="anchor",
            clip_on=False,
            color="#111111",
            zorder=5,
            bbox=dict(
                boxstyle="round,pad=0.10",
                fc="white",
                ec="#d8d8d8",
                lw=0.35,
                alpha=0.95,
            ),
        )

    if title:
        ax.set_title(title, fontsize=13, pad=12)


def place_subset_theta_labels_general(
    ax,
    masks,
    bit_angles,
    p,
    base_radius,
    bit_fontsize,
    target_mask=None,
    angle_min_sep_deg=14,
    rotation_deg=90.0,
):
    """
    General theta labels for any selected first layer.
    Labels are numbered according to the cyclic order starting from rotation_deg.
    """
    ordered = sorted(
        masks,
        key=lambda mk: cyclic_offset_from_start(
            visual_angle_for_mask(mk, bit_angles),
            rotation_deg
        )
    )
    theta_index_map = {mk: i + 1 for i, mk in enumerate(ordered)}
    items = [(visual_angle_for_mask(mk, bit_angles), mk) for mk in ordered]

    placed_angles_deg = []
    toggle = 0

    for th, mk in items:
        th_deg = angle_deg(th)
        label_r = base_radius

        if placed_angles_deg:
            prev = placed_angles_deg[-1]
            diff = abs(th_deg - prev)
            diff = min(diff, 360 - diff)
            if diff < angle_min_sep_deg:
                label_r += 0.10 if toggle % 2 == 0 else 0.18
                toggle += 1

        x, y = pol2cart(label_r, th)
        color = "#9c7300" if (target_mask is not None and is_related(mk, target_mask)) else "#222222"
        ha, va = ha_va_from_angle(th)

        theta_value = int(round(th_deg))
        theta_idx = theta_index_map[mk]
        label_text = f"{mask_to_binary(mk, p)}\n" + rf"$\theta_{{{theta_idx}}}={theta_value}^\circ$"

        ax.text(
            x,
            y,
            label_text,
            ha=ha,
            va=va,
            fontsize=bit_fontsize,
            fontweight="medium",
            color=color,
            linespacing=1.10,
            bbox=dict(
                boxstyle="round,pad=0.12",
                fc="white",
                ec="#d0d0d0",
                lw=0.4,
                alpha=0.97,
            ),
            zorder=12,
        )
        placed_angles_deg.append(th_deg)


